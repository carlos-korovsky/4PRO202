public class Calculadora {
    
    /**
     * Método estático para calculo da soma de dois números inteiros
     * @param numero1 Primeiro número a ser somado
     * @param numero2 Segundo número a ser somado
     * @return A soma de dois números
     */
    public static Integer soma(Integer numero1, Integer numero2) {
        return numero1 + numero2;
    }
    
    
}
